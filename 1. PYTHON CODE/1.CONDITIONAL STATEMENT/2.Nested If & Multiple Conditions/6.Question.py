'''
Take a day number (1–7) and print the corresponding day name. 
'''
c = int(input("Enter today day number: "))
if c == 1:
    print("Monday")
elif c == 2:
    print("Tuesday")
elif c == 3:
    print("Wednesday")
elif c == 4:
    print("Thursday")
elif c == 5:
    print("Friday")
elif c == 6:
    print("Saturday")
else:
    print("Sunnday")
